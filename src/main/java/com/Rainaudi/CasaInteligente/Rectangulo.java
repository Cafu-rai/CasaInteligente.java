package com.Rainaudi.CasaInteligente;

public class Rectangulo {

    //Atributos
    public double x, y, ancho, alto;
    public String nombre;

    //Constructor
    public Rectangulo(double x, double y, double ancho, double alto, String nombre){
        this.x = x;
        this.y = y;
        this.ancho = ancho;
        this.alto = alto;
        this.nombre = nombre;
    }
}